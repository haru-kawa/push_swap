#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (dest < src)
	{
		while (i < n)
		{
			*(unsigned char *)(dest + i) = *(unsigned char *)(src + i);
			i++;
		}
	}
	else
	{
		while (n > 0)
		{
			*(unsigned char *)(dest + (n - 1)) = *(unsigned char *)(src + (n
						- 1));
			n--;
		}
	}
	return (dest);
}
